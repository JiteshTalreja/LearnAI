from typing import List

from pydantic import BaseModel, Field


class Source(BaseModel):
    """this is the schema for source used by the agent"""

    url: str = Field(description="The URL of the source")


class AgentResponse(BaseModel):
    """this is the schema for response used by the agent"""

    answer: str = Field(description="The answer generated by the agent")
    sources: List[Source] = Field(
        default_factory=list,
        description="The list of sources used to generate the answer",
    )
